//-----------------------------------------------------------------------------------------------------------------------------
#ifndef ERROR_H_
#define ERROR_H_
//-----------------------------------------------------------------------------------------------------------------------------
#include <string>
#include <boost/lexical_cast.hpp>
//-----------------------------------------------------------------------------------------------------------------------------

namespace kmd {

        /*!
         *      Note: Consider to make this class an exceptional one!
         */
        //*********************************************************************************************************************
        class Error {
        //*********************************************************************************************************************
        public:

                //=============================================================================================================
                static const std::string message(const char* comment, int line, const char* file) {
                //=============================================================================================================
                        return std::string("<<ERROR>> file : ") + file + ", line : " + boost::lexical_cast<std::string>(line) + ", " + comment;
                }
        };
}
#endif /*ERROR_H_*/
