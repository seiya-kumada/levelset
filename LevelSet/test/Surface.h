//------------------------------------------------------------------------------------------------------------------------------------
#ifndef SURFACE_H_
#define SURFACE_H_
//------------------------------------------------------------------------------------------------------------------------------------
namespace kmd {
        //****************************************************************************************************************************
        struct Surface {
        //****************************************************************************************************************************
                Surface(int center_x, int center_y)
                        : center_x_(center_x),
                          center_y_(center_y) {
                }

                double operator()(int x, int y) const {
                        return Utilities::square(x - center_x_) + Utilities::square(y - center_y_) - Utilities::square(80);
                }

                int     center_x_;
                int     center_y_;
        };
}

#endif /*SURFACE_H_*/
